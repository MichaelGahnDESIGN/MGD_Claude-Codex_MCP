import { shellQuote } from "./shellQuote.ts";

export interface RenderSetupGuideOptions {
  generatedFiles: string[];
  outputDir: string;
  projectDir: string;
  projectName: string;
}

export function renderSetupGuide(options: RenderSetupGuideOptions): string {
  return [
    "# Claude-Codex-MCP Einfach Starten",
    "",
    `Projekt: ${options.projectName}`,
    "",
    "Diese Anleitung ist für Menschen ohne Programmierkenntnisse geschrieben.",
    "",
    "Du musst nicht verstehen, was ein MCP technisch ist. Wichtig ist nur:",
    "",
    "- Codex und Claude bekommen einen gemeinsamen lokalen Aufgabenbereich.",
    "- Dieser Aufgabenbereich liegt auf deinem Rechner.",
    "- Es werden keine Daten automatisch in eine Cloud geschickt.",
    "- Du sollst niemals Passwörter, Tokens oder private Kundendaten hineinschreiben.",
    "",
    "## Schritt 1: Was Wurde Erstellt?",
    "",
    `- Kommunikationsordner: \`${options.projectDir}\``,
    "- Datei für Menschen: `agent_comms.md`",
    "- Datei für MCP-Tools: `agent_comms.state.json`",
    `- Hilfe-Dateien: \`${options.outputDir}\``,
    "",
    "`agent_comms.md` ist die Datei, die du selbst lesen kannst. Dort stehen Aufgaben, Rückfragen, Blocker und erledigte Punkte.",
    "",
    "`agent_comms.state.json` ist die technische Datei für die KI-Tools. Diese Datei musst du nicht von Hand bearbeiten.",
    "",
    "## Schritt 2: ChatGPT Codex Einrichten",
    "",
    "Wenn du ChatGPT Codex nutzen möchtest, führe diese Datei aus:",
    "",
    "```bash",
    `bash ${shellQuote(`${options.outputDir}/codex-einrichten.sh`)}`,
    "```",
    "",
    "Danach Codex einmal schließen und neu öffnen, falls der MCP noch nicht sichtbar ist.",
    "",
    "Wenn du prüfen möchtest, ob Codex den MCP kennt:",
    "",
    "```bash",
    "codex mcp get claude-codex-mcp",
    "codex mcp list",
    "```",
    "",
    "## Schritt 3: Claude Code Einrichten",
    "",
    "Öffne diese Datei:",
    "",
    "```text",
    "claude-code-einrichten.md",
    "```",
    "",
    "Dort steht der passende Befehl für Claude Code.",
    "",
    "## Schritt 4: Claude Cowork Oder Claude Desktop Einrichten",
    "",
    "Nutze diese Datei als MCP-Konfiguration:",
    "",
    "```text",
    "claude-cowork-config.json",
    "```",
    "",
    "Wichtig ist, dass Claude und Codex denselben Kommunikationsordner nutzen.",
    "",
    "## Schritt 5: So Arbeiten Codex Und Claude Zusammen",
    "",
    "Ein einfacher Ablauf sieht so aus:",
    "",
    "1. Codex liest den aktuellen Stand.",
    "2. Codex legt eine Aufgabe für Claude an.",
    "3. Claude übernimmt die Aufgabe.",
    "4. Claude schreibt Rückfragen oder Ergebnisse hinein.",
    "5. Codex liest den neuen Stand und arbeitet weiter.",
    "",
    "Du kannst jederzeit `agent_comms.md` öffnen und nachsehen, was passiert ist.",
    "",
    "## Schritt 6: Wichtige Sicherheitsregel",
    "",
    "`AGENT_COMMS_DIR` ist nur der technische Name für den gemeinsamen Kommunikationsordner.",
    "",
    "Codex, Claude Code und Claude Cowork müssen denselben `AGENT_COMMS_DIR` nutzen. Nur dann sehen alle Agenten dieselben Aufgaben.",
    "",
    "Bitte niemals speichern:",
    "",
    "- Passwörter",
    "- API-Keys oder Tokens",
    "- `.env`-Inhalte",
    "- private Schlüssel",
    "- Datenbank-Zugänge",
    "- Kundendaten oder personenbezogene Details",
    "- private Logs oder Fehlerberichte mit sensiblen Daten",
    "",
    "Wenn solche Daten nötig wären, schreibe nur eine neutrale Rückfrage, zum Beispiel:",
    "",
    "```text",
    "Für diese Aufgabe werden Zugangsdaten benötigt. Bitte lokal prüfen und nicht in das MCP schreiben.",
    "```",
    "",
    "## Mini-Spickzettel",
    "",
    "- Vor dem Start: `read_context`",
    "- Offene Aufgaben ansehen: `list_tasks`",
    "- Aufgabe anlegen: `create_task`",
    "- Aufgabe übernehmen: `claim_task`",
    "- Rückfrage oder Hindernis: `add_blocker`",
    "- Aufgabe abschließen: `complete_task`",
    "- Dauerhafte Entscheidung: `add_decision`",
    "- Übergabe schreiben: `write_handoff`",
    "",
    "## Lokale Prüfung",
    "",
    "Wenn du prüfen willst, ob das Projekt selbst sauber läuft:",
    "",
    "```bash",
    "npm --prefix PROJEKT/WORKSPACE install",
    "npm --prefix PROJEKT/WORKSPACE run check",
    "```",
    "",
    "## Erzeugte Dateien",
    "",
    ...options.generatedFiles.map((file) => `- \`${file}\``),
    ""
  ].join("\n");
}
