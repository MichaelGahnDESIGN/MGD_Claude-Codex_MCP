import { shellQuote } from "./shellQuote.ts";

export interface RenderSetupGuideOptions {
  generatedFiles: string[];
  outputDir: string;
  projectDir: string;
  projectName: string;
}

export function renderSetupGuide(options: RenderSetupGuideOptions): string {
  return [
    "# Einfache Anleitung für Nicht-Programmierer",
    "",
    `Projekt: ${options.projectName}`,
    "",
    "## Was Wurde Erstellt?",
    "",
    `- Kommunikationsordner: \`${options.projectDir}\``,
    "- Datei für Menschen: `agent_comms.md`",
    "- Datei für MCP-Tools: `agent_comms.state.json`",
    `- Hilfe-Dateien: \`${options.outputDir}\``,
    "",
    "## ChatGPT Codex",
    "",
    "Öffne ein Terminal und starte:",
    "",
    "```bash",
    `bash ${shellQuote(`${options.outputDir}/codex-einrichten.sh`)}`,
    "```",
    "",
    "Danach Codex neu starten, falls der MCP noch nicht sichtbar ist.",
    "",
    "Prüfen:",
    "",
    "```bash",
    "codex mcp get claude-codex-mcp",
    "codex mcp list",
    "```",
    "",
    "## Claude Code",
    "",
    "Öffne die Datei `claude-code-einrichten.md` und kopiere den passenden Befehl.",
    "",
    "## Claude Cowork Oder Claude Desktop",
    "",
    "Nutze die Datei `claude-cowork-config.json` als MCP-Konfiguration.",
    "",
    "## Wichtig",
    "",
    "Codex, Claude Code und Claude Cowork müssen denselben `AGENT_COMMS_DIR` nutzen. Nur dann sehen alle Agenten dieselben Aufgaben.",
    "",
    "Keine Passwörter, Tokens, `.env`-Inhalte, Kundendaten oder privaten Logs in Aufgaben oder Chat schreiben.",
    "",
    "## Mini-Spickzettel",
    "",
    "- Vor dem Start: `read_context`",
    "- Offene Aufgaben ansehen: `list_tasks`",
    "- Aufgabe anlegen: `create_task`",
    "- Aufgabe übernehmen: `claim_task`",
    "- Rückfrage oder Hindernis: `add_blocker`",
    "- Aufgabe abschließen: `complete_task`",
    "- Dauerhafte Entscheidung: `add_decision`",
    "- Übergabe schreiben: `write_handoff`",
    "",
    "## Lokale Prüfung",
    "",
    "Wenn du prüfen willst, ob das Projekt selbst sauber läuft:",
    "",
    "```bash",
    "npm --prefix PROJEKT/WORKSPACE install",
    "npm --prefix PROJEKT/WORKSPACE run check",
    "```",
    "",
    "## Erzeugte Dateien",
    "",
    ...options.generatedFiles.map((file) => `- \`${file}\``),
    ""
  ].join("\n");
}
